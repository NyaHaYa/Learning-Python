## 튜플의 값 인덱싱, 슬라이싱, 더하기, 곱하기

## 인덱싱하기
t1 = (1,2,'a','b')
t1[0]                       # 1
t1[3]                       # 'b'

## 슬라이싱하기
t1 = (1,2,'a','b')
t1[1:]                      # (2,'a','b')

## 튜플더하기
t1 = (1,2)
t2 = (3,4)
t1 + t2                     # (1,2,3,4)

## 튜플곱하기
t1 = (1,2,3)
t1 * 3                      # (1,2,3,1,2,3,1,2,3)